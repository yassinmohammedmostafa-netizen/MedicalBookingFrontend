export type TranslationKey = keyof typeof translations;

export function useT() {
  const { lang } = useLanguage();
  return (key: TranslationKey): string => {
    const entry = translations[key];
    if (!entry) return key;
    return entry[lang] ?? entry.en;
  };
}

